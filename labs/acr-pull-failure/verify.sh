        b_temporal_records["span_seconds_deployment_to_capture"] = span_seconds
        b_strong_path_monotonic_within_30min = monotonic and span_seconds <= 30 * 60
        b_fallback_path_monotonic_within_60min = monotonic and span_seconds <= 60 * 60
    else:
        b_temporal_records["parse_error"] = "missing_one_of_three_timestamps"
