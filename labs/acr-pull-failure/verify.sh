# (acr-pull-failure).
#
# File purpose
# ------------
# Phase B verify.sh — pure file processor — emits 4 falsifiable gate JSONs
# from committed canonical evidence. This script reads ONLY the canonical
# 00-/01-/.../23- evidence files already checked into
# `labs/acr-pull-failure/evidence/` and writes the derived gate outputs:
#
#   - 30-cohort-integrity-gate.json
#   - 31-failure-attribution-gate.json
#   - 32-recovery-materialization-gate.json
#   - 33-in-place-recovery-continuity-gate.json
#
# It MUST NOT call Azure, curl remote endpoints, or depend on any live cloud
# state. A reviewer must be able to re-run this script after the lab RG has
# been deleted and still obtain the same verdict from the same committed
# evidence.
#
# Oracle ses_105f57cf7ffeKwLIxEdGFI0JmQ directives (verbatim, binding)
# -------------------------------------------------------------------
#   Q1: Keep four gates, drop Gate 31 ingress.fqdn check, drop Gate 31
#       sub-d (LA non-materialization claim), drop Gate 33 timestamp /
#       no-recreation proof, drop Gate 33 same-ACR proof unless registry
#       identity is explicit in the JSON.
#   Q2: Option B (Single canonical). 01-deployment-operations-failed.json
#       is the MANIFEST_UNKNOWN canonical source. 01-deployment-result.json
#       is optional corroboration. NO dual-path scaffolding for the
#       smoking-gun predicate.
#   Q3: Option A. PII fix in the same Phase B PR.
#   Q4: Yes. Restate the 06-h1-gate.json timestamp backfill caveat in
#       evidence/README.md and cross-reference the lab README's
#       "Honest disclosure" note.
#   Q5: Option C. The post-fix KQL silent_acceptable_post_fix
#       classification is INFORMATIONAL only, NOT promoted to a gate.
#
#   "Only use dual-path scaffolding where there are two genuinely
#    equivalent manifestations of the same claim."
#   "Bound every absence predicate to the failed-deployment cohort."
#   Target verify.sh size: 820-900 lines (~850).
#
# Numbered prefix policy
# ----------------------
# 00-/01-/.../23- = canonical committed evidence inputs (Phase A outputs).
#   This lab uses 00-14 for the H1+H2 capture sequence and 20-23 for the
#   post-run metadata (CLI versions, extension version, region, deployment
#   outputs). The 06-h1-gate.json and 14-h2-gate.json files are written by
#   Phase A scripts (trigger.sh and fix-and-capture.sh respectively) but
#   are part of the canonical cohort consumed by Phase B.
# 30-/31-/32-/33- = derived Phase B gate outputs generated by this script.
#
# The 30-33 prefix is intentional — it avoids collision with the canonical
# 20-23 files that Phase A already wrote (cli-versions, cli-containerapp-ext,
# region, deployment-outputs). If this script used 20-23 (as Lab 20 does),
# the Gate 20 JSON would overwrite the canonical 20-cli-versions.json
# artifact and orphan the evidence. Each lab's Phase B output prefix is
# chosen to skip past the highest canonical Phase A prefix in its cohort.
#
# What the four gates prove
# -------------------------
# Gate 30 = cohort integrity gate.
#   Claim: the canonical evidence pack is internally coherent before any
#   hypothesis reasoning starts. This gate consumes the 24 canonical files
#   but reasons about presence, temporal coherence of script start
#   timestamps, no-extras, and README cross-reference only.
#
# Gate 31 = failure attribution gate.
#   Claim: the failed deployment carries the MANIFEST_UNKNOWN smoking gun
#   AND the Container App resource exhibits the absence cohort
#   (provisioningState=Failed, latestRevisionName=null, revision list
#   empty, ACR repository absent) consistent with manifest resolution
#   never reaching revision creation. This is the H1 falsification check.
#   All five sub-gates use single-path predicates per Oracle Q2 directive.
#
# Gate 32 = recovery materialization gate.
#   Claim: after the operator builds and pushes labacr:v1 via az acr build
#   and updates the Container App to reference it, the platform creates a
#   Healthy revision serving 100% of traffic, the assigned FQDN returns
#   HTTP 200, and the ACR repository + tag exist. Sub-gate (c) is the only
#   dual-path predicate (curl 10/10 strong vs >=8/10 fallback) — Oracle Q1
#   directive: dual-path only where equivalent manifestations exist.
#
# Gate 33 = in-place recovery continuity gate.
#   Claim: the recovery is an IN-PLACE fix — the same Container App
#   resource identity transitions from the failed-baseline state cohort
#   to the recovered state cohort by changing only the image tag (from
#   ":does-not-exist" to ":v1") while keeping the same ACR login server.
#   This rules out the recovery being achieved by recreating a different
#   Container App resource or pointing at a different registry. Single-path
#   predicates only per Oracle Q1 directive (dropped: timestamp proof,
#   no-recreation proof, same-ACR proof relying on identity beyond what
#   the JSON encodes).
#
# Verdict semantics encoded by this script
# ---------------------------------------
# Gate 30 PASS = the evidence cohort is internally consistent.
# Gate 31 PASS = the failed-deployment cohort is fully attributable to a
#   manifest resolution failure with MANIFEST_UNKNOWN (H1 not falsified).
# Gate 32 PASS = the az acr build + az containerapp update flow materialized
#   a Healthy serving revision (H2 not falsified).
# Gate 33 PASS = the recovery is in-place (same app, same registry, only
#   the image tag changed).
#
# Gate 31 + Gate 32 together encode the lab's main claim: the existence
# of the referenced image tag in ACR is the single controlling variable
# for this failure mode. Gate 33 strengthens this by proving the recovery
# does not depend on changing any other resource identity.
#
# Claim ceiling (strict, Oracle directive)
# ----------------------------------------
# The supported claim from this evidence is:
#   "In this 2026-06-22 koreacentral reproduction, referencing a
#    non-existent ACR image tag causes a deployment-level failure with
#    MANIFEST_UNKNOWN, and rebuilding + updating to a valid tag recovers
#    the same Container App resource in-place."
# NOT:
#   "The platform always emits MANIFEST_UNKNOWN for missing tags."
#   "Recovery always completes within N minutes."
#   "ContainerAppSystemLogs_CL is never materialized in this failure mode."
# The honest-disclosure caveats in lab README + evidence/README.md
# document the empirical scope of this reproduction.
#
# QUOTED heredoc rule (mandatory)
# -------------------------------
# ALL Python heredocs in this file MUST use `<<'PY'` (single-quoted
# delimiter) to prevent shell expansion of `$` characters inside Python
# code. This is a Lab 14 lesson 29 mandatory rule. Unquoted `<<PY` would
# cause `$variable` in Python to be silently replaced by bash before the
# heredoc reaches python3.
#
# REPO_RELATIVE_EVIDENCE_DIR rule (mandatory)
# -------------------------------------------
# All gate JSONs MUST cite predicate inputs as repo-relative paths (for
# example `labs/acr-pull-failure/evidence/01-deployment-operations-failed.json`),
# NOT absolute paths. This prevents `/Users/<alias>/...` leakage into
# committed evidence and keeps the pack reproducible across operator
# machines.
#
# Record-scoped predicate rule (mandatory)
# ----------------------------------------
# Lab 15 lesson 34 — no whole-file substring searches except where the
# predicate input is a single embedded human-readable string (the
# MANIFEST_UNKNOWN smoking-gun message inside
# 01-deployment-operations-failed.json's statusMessage.error.message,
# which is itself the parsed record). Every other assertion comes from
# parsed JSON records. This script uses Python `json.load()` plus record
# iteration. It never greps the canonical JSON files to count matches.
#
# BOTH-not-OR rule (Lab 19 P0 lesson)
# ----------------------------------
# Where a hypothesis claim is about multiple required conditions, the
# gate predicate uses AND (not OR) between the conditions. Gate 31
# claims the failed-deployment cohort is *fully* attributable to manifest
# failure — every absence predicate (a-e) must hold simultaneously, not
# just one of them. Gate 33 similarly requires the identity match
# (sub-gate a) AND the state transition (sub-gate b) AND the image-tag
# delta with same registry (sub-gate c).
#
# Failed-deployment cohort binding rule (Oracle directive)
# --------------------------------------------------------
# "Bound every absence predicate to the failed-deployment cohort."
# Gate 31's absence predicates (revision list empty, ACR repository
# absent) are interpretable as evidence ONLY when read together with
# the deployment failure signal. A revision list could be empty for many
# unrelated reasons (the app was just deleted, the user typo'd the app
# name, etc.). What makes this absence diagnostic is that it co-occurs
# with a Failed deployment that carries MANIFEST_UNKNOWN. The gate JSONs
# express this by recording the deployment failure attribution in the
# same predicate_inputs block as the absence predicates, so a reader
# cannot interpret the absences without the failure context.
#
# Canonical evidence consumed by this script
# ------------------------------------------
# Gate 30 consumes:
#   ALL 24 canonical files (presence + temporal + extras + README)
#
# Gate 31 consumes:
#   01-deployment-result.json                    (provisioningState=Failed corroboration)
#   01-deployment-operations-failed.json         (MANIFEST_UNKNOWN canonical source per Oracle Q2)
#   02-containerapp-show-baseline.json           (app provisioningState=Failed, latestRevisionName=null)
