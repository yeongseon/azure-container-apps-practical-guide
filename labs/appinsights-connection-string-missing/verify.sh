#   "All Application Insights SDK paths require this env var."
#   "App Insights ingestion always completes within N seconds."
#   "Container App identity is preserved across all env-var updates."
# The honest-disclosure caveats in lab README + evidence/README.md
# document the empirical scope of this reproduction (including the :v1
# UNGUARDED variant that crashes at import and the :v2 Flask-import-order
# variant that silently fails instrumentation despite the env var being
# set).
#
# QUOTED heredoc rule (mandatory)
# -------------------------------
# ALL Python heredocs in this file MUST use `<<'PY'` (single-quoted
# delimiter) to prevent shell expansion of `$` characters inside Python
# code. This is a Lab 14 lesson 29 mandatory rule. Unquoted `<<PY` would
# cause `$variable` in Python to be silently replaced by bash before the
# heredoc reaches python3.
#
# REPO_RELATIVE_EVIDENCE_DIR rule (mandatory)
# -------------------------------------------
# All gate JSONs MUST cite predicate inputs as repo-relative paths (for
# example `labs/appinsights-connection-string-missing/evidence/01-env-before-fix.json`),
# NOT absolute paths. This prevents `/Users/<alias>/...` leakage into
# committed evidence and keeps the pack reproducible across operator
# machines.
#
# Record-scoped predicate rule (mandatory)
# ----------------------------------------
# Lab 15 lesson 34 — no whole-file substring searches. Every JSON
# predicate parses with `json.load()` and iterates parsed records. Only
# 00-trigger-run.txt and 00-verify-run.txt (human-readable run logs) are
# allowed to use string-level predicates, and even there the predicate
# is line-scoped (split on `\n`, strip, check `startswith()` and
# substring membership of a fixed marker token) — NOT whole-file regex.
#
# BOTH-not-OR rule (Lab 19 P0 lesson)
# ----------------------------------
# Where a hypothesis claim is about multiple required conditions, the
# gate predicate uses AND (not OR) between the conditions. Gate 17
# sub-gate (a) requires (env is None) AND (image endswith :v3) AND
