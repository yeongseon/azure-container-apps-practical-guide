    traffic_after_text = traffic_after_path.read_text().strip()
    before_match = STRICT_ISO_RE.match(traffic_before_text)
    after_match = STRICT_ISO_RE.match(traffic_after_text)
    before_dt = (
        datetime.fromisoformat(before_match.group(1)).replace(tzinfo=timezone.utc)
        if before_match
        else None
    )
    after_dt = (
        datetime.fromisoformat(after_match.group(1)).replace(tzinfo=timezone.utc)
        if after_match
        else None
    )
    b_temporal_records = {
        "traffic_before_raw": traffic_before_text,
        "traffic_after_raw": traffic_after_text,
        "traffic_before_iso": before_dt.isoformat() if before_dt else None,
        "traffic_after_iso": after_dt.isoformat() if after_dt else None,
        "traffic_before_parsed_strict_iso_utc_z": before_match is not None,
        "traffic_after_parsed_strict_iso_utc_z": after_match is not None,
