# verify.sh — Phase B evidence-pack verification for Lab 22
# (appinsights-connection-string-missing).
#
# File purpose
# ------------
# Phase B verify.sh — pure file processor — emits 4 falsifiable gate JSONs
# from committed canonical evidence. This script reads ONLY the canonical
# 00-/01-/.../15- + A1-/A2-/A3- evidence files already checked into
# `labs/appinsights-connection-string-missing/evidence/` and writes the
# derived gate outputs:
#
#   - 16-cohort-integrity-gate.json
#   - 17-failure-attribution-gate.json
#   - 18-recovery-materialization-gate.json
#   - 19-single-variable-falsification-gate.json
#
# It MUST NOT call Azure, curl remote endpoints, or depend on any live cloud
# state. A reviewer must be able to re-run this script after the lab RG has
# been deleted and still obtain the same verdict from the same committed
# evidence.
#
# Lab 22 binding directives (carried forward from Lab 21 Oracle review)
# ---------------------------------------------------------------------
#   Q1 (cohort binding): Bound every absence predicate to the failed-cohort
#       anchor. Gate 17 sub-gates (b)/(c) (AppRequests/AppTraces empty) are
#       interpretable as missing-env-var evidence ONLY when read together
#       with sub-gate (a) (env=null on the :v3 image's "app" container) AND
#       sub-gate (d) (the app served 20 HTTP 200 during the before-fix
#       window). Without (a)+(d), "AI tables empty" could mean SDK broken /
#       workspace misrouted / no traffic — none attributable to the missing
#       env var.
#
#   Q2 (single canonical source): Each predicate input is read from its
#       single canonical Phase A capture file. There is no dual-path
#       scaffolding for any claim that is NOT a genuinely-equivalent
#       manifestation. The only dual-path sub-gates in this verifier are
#       Gate 16 (a), (b), (c), (d) — cohort integrity tolerates a small
#       fallback band for capture variance but rejects any non-junk extras
#       and any missing canonical file.
#
#   Q3 (PII in same Phase B PR): All gate JSONs have PII placeholders
#       (00000000-0000-0000-0000-000000000000 GUIDs, user@example.com,
#       AAAA…A custom-domain-verification IDs) committed in the canonical
#       inputs already; this script does not re-introduce raw PII.
#
#   Q4 (timestamp backfill disclosure): The Lab 22 canonical inputs do NOT
#       carry a backfilled-timestamp caveat — the 02-/08- traffic-completed
#       files are single-line ISO timestamps captured at runtime by
#       fix-and-capture.sh (formerly verify.sh), and the temporal-coherence
#       sub-gate (Gate 16 sub-gate (b)) reads these runtime-captured files
#       directly. No backfill present, none used.
#
#   Q5 (informational vs gated): The A1/A2/A3 honest-disclosure files
#       document the :v1 UNGUARDED SDK crash variant for transparency.
#       They are PART OF THE COHORT (Gate 16 sub-gate (a) counts them) but
#       are NOT predicates of any failure-attribution or recovery gate.
#       The lab's canonical falsification pair is the :v3 before/after run.
#
# Numbered prefix policy
# ----------------------
# 00-/01-/.../15- + A1-/A2-/A3- = canonical committed evidence inputs
#   (Phase A outputs). This lab uses 00 for trigger+verify run logs,
#   01-15 for the H1+H2 capture sequence, and A1-A3 for honest-disclosure
#   files documenting the :v1 UNGUARDED SDK crash variant.
# 16-/17-/18-/19- = derived Phase B gate outputs generated by this script.
#
# The 16-19 prefix is intentional — it avoids collision with the canonical
# 15- file already in the cohort (15-ai-requests-detail-after-fix.json) and
# does not overwrite any Phase A artifact. Each lab's Phase B output prefix
# is chosen to skip past the highest canonical Phase A prefix in its cohort.
#
# What the four gates prove
# -------------------------
# Gate 16 = cohort integrity gate.
#   Claim: the canonical evidence pack is internally coherent before any
#   hypothesis reasoning starts. This gate consumes the 20 canonical files
#   but reasons about presence, temporal coherence of the before/after
#   traffic-completed UTC stamps, no-extras, and README cross-reference
#   only.
#
# Gate 17 = failure attribution gate.
#   Claim: the before-fix cohort carries the missing-env-var anchor
#   (01-env-before-fix.env is None on the :v3 image's "app" container)
#   AND exhibits the absence cohort (AppRequests empty, AppTraces empty)
#   AND the app served HTTP 200 throughout the capture window (so the
#   absence is attributable to missing instrumentation, NOT to the app
#   being down). This is the H1 falsification check.
#   All four sub-gates use single-path predicates per Oracle Q2 directive,
#   plus a cohort_binding_note that explicitly forbids reading sub-gates
#   (b)/(c) without sub-gates (a)+(d).
#
# Gate 18 = recovery materialization gate.
#   Claim: after the operator adds APPLICATIONINSIGHTS_CONNECTION_STRING
#   via `az containerapp update --set-env-vars`, the App Insights `requests`
#   table shows exactly [["unknown_service", 20]], the `traces` table shows
#   exactly [["unknown_service", 21]], the per-message trace records show
#   exactly 1 startup + 20 endpoint-hit messages corroborating the
#   aggregate 21, and 00-verify-run.txt shows 20 fresh HTTP 200 responses
#   plus the literal recovery PASS marker. Single-path per Oracle Q2.
#
# Gate 19 = single-variable falsification gate.
#   Claim: the recovery is IN-PLACE — the same Container App resource
#   identity transitions from the before-fix cohort to the after-fix
#   cohort by changing only the template.containers[0].env field while
#   keeping the same :v3 image, the same "app" container name, and the
#   same ACR login server registry prefix. Sub-gate (a) compares
#   `template.containers[0]` of 05- and 11- configs as JSON serialized
