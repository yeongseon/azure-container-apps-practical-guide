            deployment_dt = deployment_dt.replace(tzinfo=timezone.utc)
    else:
        deployment_dt = None

    trigger_first_line = trigger_log_path.read_text().splitlines()[0] if trigger_log_path.read_text().strip() else ""
    capture_first_line = capture_log_path.read_text().splitlines()[0] if capture_log_path.read_text().strip() else ""

    trigger_match = ISO_RE.search(trigger_first_line)
    capture_match = ISO_RE.search(capture_first_line)
    trigger_dt = (
        datetime.fromisoformat(trigger_match.group(1) + "+00:00")
        if trigger_match
        else None
    )
    capture_dt = (
        datetime.fromisoformat(capture_match.group(1) + "+00:00")
        if capture_match
        else None
    )

