# predicates (env is None on the :v3 "app" container AND the app served
# 20 HTTP 200). A reader cannot interpret "AppRequests is empty" as
# "missing env var caused no telemetry" without those anchors — empty
# tables could mean the SDK is broken, the workspace is misrouted, the
# query is wrong, or there was simply no traffic. The gate JSON
# encodes this via a top-level `cohort_binding_note` field and by
# colocating all four sub-gates' predicate_inputs in the same JSON.
#
# Canonical evidence consumed by this script
# ------------------------------------------
# Gate 16 consumes:
#   ALL 20 canonical files (presence + temporal + extras + README)
