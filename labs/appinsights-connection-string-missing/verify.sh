# strengthens this by proving the recovery does not depend on changing
# any other resource identity.
#
# Claim ceiling (strict)
# ----------------------
# The supported claim from this evidence is:
#   "In this 2026-06-22 koreacentral reproduction, an Azure Container App
#    running the :v3 image (Flask + azure-monitor-opentelemetry==1.6.4
#    with configure_azure_monitor() guarded by an env-var presence check)
