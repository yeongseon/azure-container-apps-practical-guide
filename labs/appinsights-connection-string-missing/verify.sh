#   14-revisions-lifecycle.json. Sub-gate (c) confirms same ACR registry
#   prefix and same "app" container name across the before/after pair.
#   Single-path per Oracle Q2.
#
# Verdict semantics encoded by this script
# ----------------------------------------
# Gate 16 PASS = the evidence cohort is internally consistent.
# Gate 17 PASS = the before-fix cohort is fully attributable to the missing
